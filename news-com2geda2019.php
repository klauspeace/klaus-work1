			<br />Together with CYF MOLYKO (which was also the Host Group) were CYF Bomaka, Muea, Bolifamba, Bokwai, Lysoka, and Maumu. Three(3) of the ten(10) groups of the Buea East Zone did not take part in this come-together due to circumstances beyond human control. We pray that these groups; Ekona town, Ekona Yard and Mautu, come back and join us as the Holy Spirit will work things out for us. <br />
			<br />This event took place concurrently with the zonal come-together of two other zones of the Fako North Presbytery; MUYUKA AND PENDAMBOKO Zones. Among the five zones in this presbytery, the Buea Zone had held their come-together earlier on  the previous Sunday, 15th September 2019 at P.C.C Likoko Membea. The Bafia Zone had not been functional since 2016 due to circumstances beyond human control. We pray God that they come back on board.
			</p>
                        
