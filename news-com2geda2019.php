			</p>


			<p style="font-size: 23px">The Parish Pastor of P.C.C Molyko, <strong style="color: #3c529d;">Rev. Tah Christopher Fobang</strong>, in his sermon captioned "<strong style="color: red; text-transform: uppercase;">Let Nothing Hinder you in your service to God</strong>", drawn from <strong style="color: red;">Acts 5:42</strong> encouraged the christians of P.C.C Molyko and the youths of the Buea East Zone to serve God wholeheartedly, preach the good news, witness for Christ and be strong and stand firm in the faith even in the midst of persecution. And above all, depend on the guidiance of the Holy Spirit who empowers and motivates us. All these will enable us to render genuine <strong style="color: #3c529d;">Acts of Service to God</strong>.
